        <div class="ancho-texto">
          <p class="resumen">
            El gobierno mexicano carece de una política efectiva de prevención y
            actuación ante la violencia sexual dentro de las aulas. El único
            intento por homologar a nivel nacional los protocolos de atención
            para combatir esta problemática fracasó
          </p>
          <p>
            Una decena de abusos y violaciones sexuales cometidos en contra de
            niños y niñas del Jardín de niños Montessori Matatena, un colegio
            privado en la Ciudad de México, puso en jaque al gobierno mexicano.
            Era 2016 y la administración pública aún no tenía lineamientos que
            dieran atención y seguimiento a los delitos sexuales cometidos por
            el personal de los colegios.
          </p>
          <p>
            Apenas unos meses antes, el Gobierno de Enrique Peña Nieto había
            convocado a especialistas de distintas áreas para buscar soluciones
            a la violencia en el país, consideraron la escolar. Encontraron que
            para combatir la violencia en las escuelas era necesario crear
            protocolos de actuación —incluyendo los que servían para atender el
            abuso sexual—, identificar a las que tienen mayores problemas,
            generar indicadores y estadísticas. Pero todo quedó en sugerencia.
          </p>
          <p>
            Por eso, cuando los padres del Jardín de Niños Montessori Matatena
            acudieron a los medios de comunicación a evidenciar las omisiones de
            las autoridades educativas, al Gobierno no le quedó más que pedir
            perdón.
          </p>
          <p>
            No era la primera vez que una historia de abusos sexuales cometidos
            contra niñas y niños dentro de las escuelas a las que asisten salía
            a la luz. Hay cientos de antecedentes registrados escuetamente en la
            prensa, incluyendo aquel de 2011 en el que se reveló que el conserje
            del Jardín de Niños Andrés Oscoy, en Iztapalapa, había violentado
            sexualmente al menos a 30 niños. Sin embargo, en este caso, las
            familias y el primer abogado defensor de las víctimas lograron
            mantener el caso Matatena en medios de comunicación durante meses,
            obligando al Gobierno mexicano a pronunciarse públicamente.
          </p>
          <p>
            Aurelio Nuño, titular de la Secretaría de Educación Pública (SEP) en
            ese entonces, admitió que el sistema no estaba funcionando. Tras una
            investigación, el gobierno mexicano reconoció que existió una “clara
            laxitud, falta de pericia, sensibilidad y oportunidad en el actuar
            de los servidores competentes” en el caso.
          </p>
          <h3>“Tu hijo está siendo abusado”</h3>
          <p>
            La cadena de omisiones era evidente. A finales de 2015 un padre de
            un alumno del kínder Montessori Matatena, ubicado en la alcaldía
            Benito Juárez —una de las zonas con mayor plusvalía de la Ciudad de
            México— solicitó a la Autoridad Educativa Federal de la Ciudad de
            México (AEFCM), órgano que depende de la SEP, investigar la
            presencia cotidiana del esposo de la directora en el colegio. Le
            parecía sospechoso que convivía demasiado con los niños.
          </p>
          <p>
            Por Ley, la AEFCM estaba obligada a vigilar que la escuela privada
            brindara el servicio garantizando la salud, seguridad y dignidad de
            los educandos, pero fue omisa ante las quejas que recibió antes de
            que los abusos salieran a la luz.
          </p>
          <p>
            Cuatro meses después, la preocupación resultó fundada. Un niño de
            tres años le dijo a su cuidadora: “te voy a dar medicina” y, al
            tiempo, intentó poner su pene en la boca de la mujer. Ante esta
            señal de alerta, la madre llevó al pequeño con una psicóloga
            infantil. “Tu hijo está siendo abusado”, le confirmó. El agresor,
            detalló el pequeño, era Rafael D., esposo de la directora, y quien
            lo obligaba a “jugar al doctor”.
          </p>

          <img src="~/assets/imgs/cap2/ilustracion_2.jpg" alt="" />
          <p>
            Ante las acusaciones, la directora y dueña del colegio negó todo y
            pidió a la madre no ejercer ninguna acción penal. No emitió el
            reporte interno sobre los señalamientos, tampoco notificó a las
            autoridades educativas ni denunció el delito. La madre dio aviso a
            otras familias y terminó por destapar la historia de una década de
            abusos y violaciones sexuales cometidas por el hombre en contra de
            los preescolares que asistían al colegio.
          </p>
          <p>
            Luis Ignacio Sánchez, quien era titular de la AEFCM, reconoció:
            “Nuestros protocolos de prevención, como tal, no existen, no
            funcionan y hay que hacer algo”. La Comisión Investigadora de la SEP
            confirmó: hubo negligencia, falta de sensibilidad e ineficiencia en
            la supervisión escolar.
          </p>
          <p>
            En medio de la presión mediática, el Gobierno fortaleció el Programa
            Nacional de Convivencia Escolar (PNCE) que tenía como misión brindar
            asistencia a los estados para desarrollar técnicas y manuales de
            prevención, detección y atención a las violencias en las aulas,
            incluyendo la sexual. También permitía costear capacitaciones para
            el personal, una forma de prevenir las violencias y atenderlas
            oportunamente.
          </p>
          <p>
            En ese marco, la SEP produjo las orientaciones generales para que
            cada entidad federativa creara, según sus necesidades, protocolos
            que evitaran casos de violencia sexual en las escuelas. La finalidad
            era que se aplicaran de manera obligatoria en todas las escuelas
            públicas y privadas de educación básica. Se buscaba claridad en el
            abanico de responsabilidades, pues una agresión no queda sólo en
            manos de quien la comete, incluye al resto del personal de la
            escuela que fungen como cuidadores y garantes de derechos y abarca a
            las secretarías de Educación estatales que vigilan que las escuelas
            sean espacios libres de violencia. La Ley General de Educación (LGE)
            lo marca así.
          </p>
          <p>
            Parte del presupuesto asignado se destinó al desarrollo de los
            protocolos estatales —la información pública no permite establecer
            cuánto costó la creación de estas guías.
          </p>
          <p>
            En 2016, la SEP gastó 73.5 millones de pesos en el PNCE, según el
            ejercicio capturado en la Cuenta Pública. El monto fue subiendo
            paulatinamente año con año, llegando a superar el gasto anual de 200
            millones.
          </p>
          <p>
            A la SEP le tomó tres años lograr que las 32 entidades tuvieran
            protocolos para dar atención a la violencia sexual en las aulas.
            “Esta iniciativa ha homologado las acciones con las que se contaba y
            actualmente favorece la detección temprana de factores de riesgo en
            los estudiantes", se lee en su sexto
            <a
              href="https://www.planeacion.sep.gob.mx/Doc/informes/labores/2012_2018/6to_informe_de_labores.pdf"
              >informe</a
            >
            de labores.
          </p>
          <p>
            Estos informes no contienen los resultados de la efectividad de la
            creación de estos documentos ni el seguimiento de la autoridad
            federal a lo implementado por cada estado del país. Por si fuera
            poco, el PNCE dejó de existir en 2021, durante la administración de
            Andrés Manuel López Obrador. A partir de entonces, la SEP dejó de
            mencionar la atención al abuso sexual infantil como una de sus
            líneas de acción.
          </p>
          <h3>Protocolos de papel</h3>
          <p>
            Pese al intento gubernamental por homologar la prevención y atención
            a la violencia sexual en las aulas a nivel nacional, casos
            reportados ante comisiones de derechos humanos y procuradurías de
            defensa de la infancia, obtenidos para esta investigación, muestran
            que las omisiones de las autoridades siguen presentes.
          </p>
          <p>
            En 2018, un maestro de 3° de primaria agredió sexualmente a seis
            niñas en una escuela del municipio de Aquismón, San Luis Potosí; la
            investigación concluyó que la directora no implementó acciones de
            vigilancia y seguridad efectivas para salvaguardar la integridad
            física y psicológica de los menores que tenía bajo su cargo, según
            quedó asentado en la recomendación
            <a
              href="http://www.cedhslp.org.mx/Web2/Recomendaciones/2019/Recomendacion%2027/Recomendacion%20-%2027-19.pdf"
              >27/2019</a
            >, que contiene la investigación que realizó la Comisión de derechos
            Humanos. En otro caso, el del Jardín de niños “María Lavalle Urbina”
            en Tecate, Baja California, se comprobó que el conserje había
            abusado sexualmente de seis niñas de cuatro años en 2018. Aunque el
            ministerio público ordenó su aprehensión, ésta se concretó cinco
            años después, pues el hombre gozó de licencias sindicales que
            impidieron su localización oportuna.
          </p>
          <p>
            Como parte de esta investigación se corroboró que los protocolos se
            crearon con una falla de origen: son documentos no vinculantes,
            guías que marcan la pauta, pero que no establecen ninguna sanción
            para quien las incumple.
          </p>
          <p>
            La Clínica para la Protección y Restitución de Derechos de Niñas,
            Niños y Adolescentes de la Universidad Iberoamericana, la Oficina de
            Defensoría de los Derechos de la Infancia, A.C. (ODI) y El Universal
            contrastaron los protocolos estatales con los estándares que se han
            desarrollado a nivel internacional sobre cómo deben actuar las
            autoridades ante este tipo de casos y encontraron varias falencias.
          </p>
